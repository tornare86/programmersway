package PDF;

public class Program2_01 {

	public static void main(String[] args) {

		int n = 5;

		// [01]
		System.out.print("2-1) number = 5\n");
		System.out.println();

		for (int i = 0; i < n; i++) {
			for (int j = 0; j < n; j++)
				System.out.printf("*");
			System.out.println();
		}

	}
}
